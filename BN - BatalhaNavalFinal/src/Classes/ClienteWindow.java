    private void jbJogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbJogarActionPerformed
        // TODO add your handling code here:
        String jsonCampo;
        Gson gson = new Gson();

        if (mCampo1.validaQtdBombas(mCampo1.iMatrizCampo)) {
            jtCampo1.setEnabled(false);
            jtCampo1.setCellSelectionEnabled(false);
            jbJogar.setEnabled(false);
            jbJogar.setText("O jogo começou. Ataque seu inimigo!");
            bBlock = true;
            jsonCampo = gson.toJson(mCampo1.iMatrizCampo);

            try {
                tcpClient.sendCampo("N" + jtxtPlayer.getText() + "C" + jsonCampo); //enviado para o servidor
            } catch (Exception ex) {
                System.out.println(ex.getMessage());
            }
        } else {
            jtCampo1.setEnabled(true);
            jtCampo1.setCellSelectionEnabled(true);
            jbJogar.setEnabled(true);
            jbJogar.setText("Clique aqui para iniciar o jogo!");
            bBlock = false;
            JOptionPane.showMessageDialog(this,
                    "Tem que por 10 bombas aí mano!", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_jbJogarActionPerformed

    public void escreverMensagem(String sMensagem) {
        iPontos++;
        jogador = sMensagem.substring(sMensagem.indexOf("N") + 1);

        if (sMensagem.substring(0, 1).equals("P")) {
            jlMensagem.setText("Poutz! Acertou.");
            jlMensagem.setForeground(Color.green);
            jtxtPtoPro.setText(Integer.toString(Integer.parseInt(jtxtPtoPro.getText()) + 1));
            if (jtxtPtoPro.getText().equals("10")) {
                JOptionPane.showMessageDialog(this,
                        "Você ganhou!", "Fim de Jogo!", JOptionPane.INFORMATION_MESSAGE);
            }
        } else if (sMensagem.substring(0, 1).equals("E")) {
            jlMensagem.setText("Há! Você errou!");
            jlMensagem.setForeground(Color.red);
        } else if (sMensagem.substring(0, 1).equals("X")) {
            jtxtPtoContra.setText(Integer.toString(Integer.parseInt(jtxtPtoContra.getText()) + 1));
            if (jtxtPtoContra.getText().equals("10")) {
                JOptionPane.showMessageDialog(this,
                        "Você perdeu!", "Fim de Jogo!", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }

    public void confTela(int iConf) {

        if (iConf == 0) {//conf para jogar
            jTextField1.setEnabled(false);
            jtxtPlayer.setEnabled(false);
            jbConectar.setEnabled(false);
            jbDesconectar.setEnabled(true);
            jtCampo1.setEnabled(true);
            jtCampo1.setCellSelectionEnabled(true);
            jtCampo2.setEnabled(true);
            jbJogar.setEnabled(true);
        } else { //config para conectar
            jTextField1.setEnabled(true);
            jtxtPlayer.setEnabled(true);
            jbConectar.setEnabled(true);
            jbDesconectar.setEnabled(false);
            jtCampo1.setCellSelectionEnabled(false);
            jtCampo1.setEnabled(false);
            jtCampo2.setEnabled(false);
            jbJogar.setEnabled(false);
        }
    }

