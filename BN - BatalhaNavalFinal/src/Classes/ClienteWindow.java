    private void jbDesconectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDesconectarActionPerformed
        DefaultTableModel tableModel1 = (DefaultTableModel) jtCampo1.getModel();
        DefaultTableModel tableModel2 = (DefaultTableModel) jtCampo2.getModel();
        try {
            tcpClient.closeConnection();
            jTextField1.setText("");
            jlQtdBombas.setText("0");
            confTela(1); //para conectar
            bBlock = false;

            for (int i = 0; i < 7; i++) {
                for (int j = 0; j < 7; j++) {
                    tableModel1.setValueAt(null, i, j);
                    tableModel2.setValueAt(null, i, j);
                }
            }
        } catch (IOException ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_jbDesconectarActionPerformed

    private void jtCampo1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtCampo1MouseClicked
        // TODO add your handling code here:
        int iColumn, iRow;

        DefaultTableModel tableModel1 = (DefaultTableModel) jtCampo1.getModel();

        if (bBlock == false) {

            Object oValue = new Object();

            iRow = jtCampo1.getSelectedRow();
            iColumn = jtCampo1.getSelectedColumn();

            evt.consume();

            try {

                if (iRow != -1) {
                    oValue = tableModel1.getValueAt(iRow, iColumn);
                    if (oValue == null) {
                        tableModel1.setValueAt("X", iRow, iColumn);
                        mCampo1.setValue(iRow, iColumn);
                        jlQtdBombas.setText(Integer.toString(Integer.parseInt(jlQtdBombas.getText()) + 1));

                    } else {
                        tableModel1.setValueAt(null, iRow, iColumn);

                        mCampo1.remValue(iRow, iColumn);
                        jlQtdBombas.setText(Integer.toString(Integer.parseInt(jlQtdBombas.getText()) - 1));
                    }
                }
            } catch (Exception ex) {
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_jtCampo1MouseClicked

